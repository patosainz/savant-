    <p class="intro">A los estadios de la <a href="alopecia-androgenetica.php">alopecia androgénica</a> masculina se clasifican en siete niveles (I a VII) según la escala desarrollada por el doctor James Hamilton en 1951 y posteriormente modificada y ampliada por el doctor O’Tar Norwood en 1975. El 80% de los hombres afectados por alopecia androgénica pasan por las siete etapas establecidas. La escala Hamilton-Norwood estandariza los estadíos de la pérdida de cabello y permite la documentación científica. La escala es una herramienta muy valiosa para el tratamiento de extracción de Unidades Foliculares y las terapias preventivas.</p>
