	<h2>Estadios de la Alopecia Androgénica</h2>
    <p class="intro">El desarrollo típico de la alopecia androgénica inicia en las zonas temporal y frontal. Posteriormente empieza a escasear el pelo por la coronilla, hasta que la calva se extiende por toda la parte superior de la cabeza. En la mayor parte de los casos se mantiene una franja de pelo con forma de corona por la nuca y encima de las orejas. La escala clasifica la calvicie en los siguientes tipos (ver figura):</p>
	<p align="center"><img src="imagenes/contenidos/escala-hamilton-norwood.jpg" width="756" height="158" alt=""/></p>
    <ol type="I">
		<li>Pérdida de cabello inapreciable o escasa en la parte frontal.</li>
		<li>Caída del cabello por la zona temporal; inician las entradas.</li>
		<li>Pérdida de cabello especialmente por la zona de la coronilla. No se aprecia pérdida de cabello adicional en la zona frontal. Es el nivel a partir del cual se considera que hay calvicie.</li>
		<li>Se amplía la zona sin cabello en la coronilla. Una banda de cabello separa claramente las dos zonas: entradas y coronilla.</li>
		<li>Las zonas de la coronilla y de las entradas están separadas solamente por una franja de cabello muy estrecha.</li>
		<li>Las entradas y la coronilla se juntan y se ensancha zona sin cabello.</li>
		<li>En este estadio solamente queda una porción estrecha de cabello, que se extiende sobre las orejas y se junta en la nuca.</li>
	</ol>

	<p class="intro">Para los tipos III a V existe una subdivisión, las conocidas como variantes A y V. En la variante A la caída de cabello se concentra solo en la parte de la frente, mientras que en la V va acompañada de pérdida en la coronilla.</p>

