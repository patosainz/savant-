    <p style="margin-top:0px;">En el 2009 debido a la demanda la empresa decide expandirse a la ciudad de Monterrey y se le asigna el cargo de Director Médico de la clínica de micro implante capilar. Aquí se encarga de renovar la técnica de micro implante y de tratamientos capilares mixtos. </p>
    <p>Actualmente es Miembro de la “International Society of Hair Restoration Surgery” y tiene en su haber más de 1,000 procedimientos de micro implante capilar utilizando la técnica de Extracción de Unidades Foliculares.</p>
