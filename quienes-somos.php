    <p> Inicia su trayectoria en el micro implante capilar en el año 2007 en una renombrada clínica donde implementa y estandariza controles de esterilización y calidad, llevándola a ocupar el puesto de jefa de quirófano. En el año 2009 es trasladada a la clínica de Monterrey con el cargo de Jefa de control de calidad, esterilización y enfermería, así como asistente médico en el área de micro implante capilar.</p>
