	<h4>Que es la alopecia, etiologías de la alopecia y tratamiento de la alopecia androgenética</h4>
    <p class="intro">Alopecia: Síndrome que produce la caída del cabello por diferentes etiologías, de entre las más comunes se pueden mencionar: La alopecia androgenética o alopecia androgénica (AGA), la alopecia traumática, alopecia areata, alopecia difusa, alopecia farmacológica y alopecia por dermatosis.</p>
    <p class="intro">La Alopecia Androgenética es el tipo más común de pérdida de cabello en el hombre. Es el resultado de un proceso continuo de un tipo específico de alopecia que sigue un patrón definido en individuos que están genéticamente predispuestos. Esta condición andrógeno-dependiente es a menudo referida como alopecia masculina, alopecia común o calvicie.</p>
