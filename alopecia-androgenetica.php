	<h2>Objetivo del tratamiento de la Alopecia Androgenética</h2>
    <p class="intro">Una vez entendido el proceso de la Alopecia Androgenética se puede establecer que el objetivo de su tratamiento es la restauración de las zonas afectadas por dicho padecimiento. Este tratamiento se puede aplicar farmacológicamente y/o con micro implante capilar mínimamente invasivo conocido como técnica de Extracción de Unidades Foliculares (FUE - Follicular Unit Extraction). </p>
    <p class="intro">Esta técnica también es ampliamente conocida como Trasplante o Transferencia Folicular (FT – Folicular Transfer). El tratamiento debe de ser aplicado por un equipo médico especializado para la realización y evolución adecuada de dichos procedimientos apegándose a los principios de ética médica, profesionalismo, responsabilidad, compromiso y normatividad necesaria para llevar a cabo con éxito los objetivos planeados.</p>   
