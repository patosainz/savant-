	<h2>Antecedentes de la técnica de Extracción de Unidades Foliculares (FUE)</h2>
    <p class="intro">En la reunión de 2007 de la International Society of Hair Restoration Surgery (ISHRS) se habló por primera vez de los dispositivos mecanizados para la Extracción de Unidades Foliculares. La investigación sobre dispositivos robóticos fue presentada por el doctor Miquel G. Canales y el doctor David Berman en la reunión de 2008. Investigación realizada por el Berman Skin Institute de Palo Alto, California encontró que los folículos se podrían quitar individualmente a través del uso de agujas huecas de 1 mm.  </p>
    <p class="intro">Existen pocos cirujanos especialistas que dominen extracción de la unidad folicular porque aprender el procedimiento requiere de mucho tiempo y una inversión económica muy elevada para desarrollar una buena habilidad.</p>
    <p class="intro"> Actualmente la técnica de Extracción de Unidades Foliculares se encuentra en desarrollo siguiendo siempre un solo objetivo: eliminación de las secuelas permanentes en el área donadora (cicatrices, insensibilidad, etc.) así como por medio de la experiencia y el sentido de las proporciones del rostro y cabeza obtener resultado más estético y natural dependiendo de cada individuo. </p>

