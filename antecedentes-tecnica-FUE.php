	<h4>Inicio y evolución de la técnica FUE: Extracción de Unidades Foliculares</h4>
    <p class="intro">La técnica de Trasplante Folicular fue descrita inicialmente en Japón por Masumi Inaba en los 40´s (M. Inaba, Y. Inaba, Androgenetic Alopecia, Modern Concepts of Pathogenesis and Treatment, Springer Japan, 1996), cuando utilizó una aguja de 1 mm para realizar la extracción de unidades foliculares individuales. El Dr. Orentrik continuó con los estudios en Nueva York en 1959 e introdujo el uso del “punch” para extraer unidades foliculares y redistribuirlas al área alopécica sin embargo el diámetro del instrumento era no menor de 8 mm. No fue sino hasta la década pasada cuando los doctores Rassman y Bernstein describen la técnica de Extracción de Unidades Foliculares en su publicación “Follicular Unit Extraction: Minimally Invasive Surgery for Hair Transplantation”</p>
